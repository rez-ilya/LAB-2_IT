#include <iostream>
using namespace std;

void leftRotate(int* arr, int n, int k) {
    // Создаем временный массив для первых k элементов
    int* temp = new int[k];
    for (int i = 0; i < k; i++) {
        *(temp + i) = *(arr + i); // Копируем первые k элементов в temp
    }

    // Сдвигаем оставшиеся элементы влево
    for (int i = 0; i < n - k; i++) {
        *(arr + i) = *(arr + i + k);
    }

    // Копируем элементы из temp в конец массива
    for (int i = 0; i < k; i++) {
        *(arr + n - k + i) = *(temp + i);
    }

    // Освобождаем память, выделенную для временного массива
    delete[] temp;
}

int main() {
    setlocale(LC_ALL, "Russian");
    int n, k;

    cout << "Введите количество элементов массива n: ";
    cin >> n;

    while (n <= 0) {
        cout << "Ошибка: размер массива должен быть больше 0." << endl;
        cout << "Введите количество элементов массива n: ";
        cin >> n;
    }

    cout << "Введите число сдвига k (0 < k < n): ";
    cin >> k;

    while (k <= 0 || k >= n) {
        cout << "Ошибка: k должно быть больше 0 и меньше n." << endl;
        cout << "Введите число сдвига k (0 < k < n): ";
        cin >> k;
    }

    // Динамически выделяем память для массива
    int* arr = new int[n];

    cout << "Задайте массив arr: " << endl;
    for (int i = 0; i < n; i++) {
        cout << "Введите число *(arr + " << i << ") : ";
        cin >> *(arr + i); // Вводим элементы с помощью указателей
    }

    // Выполняем циклический сдвиг влево
    leftRotate(arr, n, k);

    // Выводим сдвинутый массив
    cout << "Результат после циклического сдвига влево на " << k << " элементов:" << endl;
    for (int i = 0; i < n; i++) {
        cout << *(arr + i) << " "; // Используем указатель для вывода
    }

    // Освобождаем память, выделенную для массива
    delete[] arr;

    return 0;
}